async function initialize() {
  const provider = new ethers.providers.Web3Provider(window.ethereum);
  const signer = provider.getSigner();
  const contract = new ethers.Contract(contractAddress, abi, signer);

  document.getElementById("mineButton").addEventListener("click", async () => {
    const statusDiv = document.getElementById("status");
    try {
      statusDiv.textContent = "Mining token...";
      const tx = await contract.mineToken(); // Assuming `mineToken` is the function in your contract
      await tx.wait();
      statusDiv.textContent = "Token mined successfully!";
    } catch (error) {
      console.error(error);
      statusDiv.textContent = `Error: ${error.message}`;
    }
  });
}

if (typeof window.ethereum !== "undefined") {
  window.ethereum.request({ method: "eth_requestAccounts" }).then(() => {
    initialize();
  }).catch((err) => {
    console.error(err);
    document.getElementById("status").textContent = "MetaMask connection required.";
  });
} else {
  document.getElementById("status").textContent = "MetaMask is not installed.";
}
